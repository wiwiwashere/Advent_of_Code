auto distance(vector<int>& l1, vector<int>& l2)
{
    auto total = 0;

    for (int i = 0; i < l1.size(); i++)
    {
        int absolute = abs(l1[i] - l2[i]);
        total += absolute;
    }
    return total;
}

auto similar(vector<int>& l1, vector<int>& l2)
{
    auto score = 0;

    for (int i = 0; i < l1.size(); i++)
    {
        int frq_l2 = 0;
        for (int y = 0; y < l2.size(); y++)
        {
            if (l1[i] == l2[y])
            {
                frq_l2++;
            }
        }
        score += frq_l2 * l1[i];
    }
    return score;
}

